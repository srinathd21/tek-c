            SUM(CASE WHEN payment_status = 'Paid' THEN 1 ELSE 0 END) as paid_count,
            SUM(net_salary) as total_earned,
            AVG(net_salary) as avg_salary
        FROM payslips
        WHERE employee_id = ?
