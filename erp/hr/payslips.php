function formatCurrency($amount)
{
    if (!$amount || $amount == 0)
        return '—';
    return '₹ ' . number_format($amount, 2);
}

function formatMonthYear($month, $year)
{
    return date('F Y', mktime(0, 0, 0, $month, 1, $year));
}

function getPayslipStatusBadge($status)
{
