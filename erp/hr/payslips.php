    $types .= "i";
}

if ($year_filter !== 'all') {
    $query .= " AND p.year = ?";
    $params[] = $year_filter;
    $types .= "i";
}

if ($status_filter !== 'all') {
    $query .= " AND p.payment_status = ?";
    $params[] = $status_filter;
    $types .= "s";
}

if ($employee_filter > 0 && !$isEmployee) {
    $query .= " AND p.employee_id = ?";
    $params[] = $employee_filter;
    $types .= "i";
}

